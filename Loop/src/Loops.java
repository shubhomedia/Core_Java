public class Loops {
    public static void main(String[] args) {
        System.out.println("1");
        System.out.println("2");
        System.out.println("3");
        System.out.println("4");
        System.out.println("5");

        int counter = 0;
        while (counter < 10) {
            counter = counter + 1;
            System.out.println(counter);
        }
    }
}
